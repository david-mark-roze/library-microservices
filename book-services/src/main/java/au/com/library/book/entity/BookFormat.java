package au.com.library.book.entity;

/**
 * Defines the possible formats for a library book.
 */
public enum BookFormat {

    /**
     * A physical book with a hard cover.
     */
    HARDBACK,
    /**
     * A physical book with a soft cover.
     */
    PAPERBACK,
    /**
     * A graphic novel or comic book format.
     */
    COMIC,
    /**
     * An electronic version of the book.
     */
    DIGITAL,
    /**
     * An audio version of the book.
     */
    AUDIO

}
